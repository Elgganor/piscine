void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int	i;
	int	j;

	i = 0;
	j = (size - 1);
	while (i++ < (size / 2))
	{
		tmp = tab[i - 1];
		tab[i - 1] = tab[j];
		tab[j--] = tmp;
	}
}
