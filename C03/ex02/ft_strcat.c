char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	while (dest[++i])
		;
	while (src[j++])
		dest[(i - 1) + j] = src[j - 1];
	dest[(i - 1) + j] = '\0';
	return (dest);
}
