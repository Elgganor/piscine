#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
		ft_putchar('-');
	if (nb >= 10 || nb <= -10)
		ft_putnbr((nb < 0) ? -(nb / 10) : (nb / 10));
	ft_putchar((nb < 0) ? -(nb % 10) + '0' : (nb % 10) + '0');
}
